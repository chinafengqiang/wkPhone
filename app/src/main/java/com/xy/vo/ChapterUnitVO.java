public class ChapterUnitVO {
    private int chapterUnitId;
    private String chapterUnitName;
    private int chapterUnitPid;
    private String chapterUnitUrl;
    private String chapterUnitDesc;
    private String chapterUnitVideoUrl;

    public int getChapterUnitId() {
        return chapterUnitId;
    }

    public void setChapterUnitId(int chapterUnitId) {
        this.chapterUnitId = chapterUnitId;
    }

    public String getChapterUnitName() {
        return chapterUnitName;
    }

    public void setChapterUnitName(String chapterUnitName) {
        this.chapterUnitName = chapterUnitName;
    }

    public int getChapterUnitPid() {
        return chapterUnitPid;
    }

    public void setChapterUnitPid(int chapterUnitPid) {
        this.chapterUnitPid = chapterUnitPid;
    }

    public String getChapterUnitUrl() {
        return chapterUnitUrl;
    }

    public void setChapterUnitUrl(String chapterUnitUrl) {
        this.chapterUnitUrl = chapterUnitUrl;
    }

    public String getChapterUnitDesc() {
        return chapterUnitDesc;
    }

    public void setChapterUnitDesc(String chapterUnitDesc) {
        this.chapterUnitDesc = chapterUnitDesc;
    }

    public String getChapterUnitVideoUrl() {
        return chapterUnitVideoUrl;
    }

    public void setChapterUnitVideoUrl(String chapterUnitVideoUrl) {
        this.chapterUnitVideoUrl = chapterUnitVideoUrl;
    }
}
